package net.tarotpoker.core;

public class TPRuntimeException extends RuntimeException {

	private static final long serialVersionUID = -5188649709453079350L;

	public TPRuntimeException() {
		super();
	}

	public TPRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TPRuntimeException(String arg0) {
		super(arg0);
	}

	public TPRuntimeException(Throwable arg0) {
		super(arg0);
	}

	
}
